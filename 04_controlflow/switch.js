// SWITCH-CASE Syntax

// switch (key) {
//     case value:
//         break;

//     default:
//         break;
// }

const count = 5

switch (count){ 
    case 1:
        console.log("ONE");
        break;

    case 2:
        console.log("TWO");
        break;
    
    default:
        console.log("MORE THAN TWO");
        break;
}

